	public static String mins_to_string(int mins)
	{
		String[] arStr = {"day", "hr", "min"};
		int[] arDiv = {1440,60, 1};


		// specifically handle zero
		if (mins == 0 ) return "0 secs";
		String retVal = "";
		for(int i=0; i<3; ++i){
			int q = mins/arDiv[i];
			if( q > 0 ){
				retVal += Integer.toString(q) + arStr[i];
				mins -= q * arDiv[i];
			}
		}

		return retVal;
	}

	public static String secs_to_string(int secs)
	{
		String[] arStr = { "hr", "min", "sec"};
		int[] arDiv = {3600,60, 1};


		// specifically handle zero
		if (secs == 0 ) return "0 secs";
		String retVal = "";
		for(int i=0; i<3; ++i){
			int q = secs/arDiv[i];
			if( q > 0 ){
				retVal += Integer.toString(q) + arStr[i];
				secs -= q * arDiv[i];
			}
		}

		return retVal;

	}
